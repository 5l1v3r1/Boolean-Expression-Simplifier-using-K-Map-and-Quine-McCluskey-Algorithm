package application;

public class Row {
	int a,b,c,d,result;

	public Row(int a, int b, int c, int d, int result) {
		this.a = a;
		this.b = b;
		this.c = c;
		this.d = d;
		this.result = result;
	}

	public int getA() {
		return a;
	}

	public void setA(int a) {
		this.a = a;
	}

	public int getB() {
		return b;
	}

	public void setB(int b) {
		this.b = b;
	}

	public int getC() {
		return c;
	}

	public void setC(int c) {
		this.c = c;
	}

	public int getD() {
		return d;
	}

	public void setD(int d) {
		this.d = d;
	}

	public int getResult() {
		return result;
	}

	public void setResult(int result) {
		this.result = result;
	}

	
	
	

}
